const CONTRACT_ADDRESS = "0x8A40Fe3fc3A490Ce093d34F9B0F905f4a648c28d";

function App() {
  let user_account;
  let mintedAmount = 0;
  let totalAmount = 20;

  async function onClick_Wallet() {
    const accounts = await window.klaytn.enable();
    const caver = new Caver(window.klaytn);

    const account = accounts[0];
    user_account = account;
    await caver.klay.getBalance(account).then((balance) => {
      console.log(balance);
      alert("보유 잔액: " + caver.utils.fromPeb(balance, "KLAY"));
    });
    // const account = accounts[0]
    // const selectedAddress = window.klaytn.selectedAddress
    console.log(accounts);
    console.log(window.klaytn.networkVersion);
    console.log(window.klaytn.selectedAddress);

    window.klaytn.on("accountsChanged", function (accounts) {
      console.log(window.klaytn.selectedAddress);
    });

    window.klaytn.on("networkChanged", function () {
      console.log(window.klaytn.networkVersion);
    });
  }

  async function onClick_Mint() {
    const caver = new Caver(window.klaytn);
    const contract = new caver.klay.Contract(abi, CONTRACT_ADDRESS);
    console.log(contract);

    let estimated_gas;

    const mintAmount = 1;
    const mintFee = 1000000000000000;
    const total_value = mintAmount * mintFee;

    contract.methods
      .mint(mintAmount)
      .send({
        from: user_account,
        gas: 8000000,
        value: total_value,
      })
      .then((receipt) => {
        alert("민팅 성공: " + JSON.stringify(receipt));
      })
      .catch((error) => {
        alert("민팅 실패: " + error);
        console.log(error);
      });
  }

  return (
    <div className="App">
      <header className="App-header">
        <label>
          {mintedAmount}/{totalAmount}
        </label>
        <button onClick={onClick_Wallet}>Wallet</button>
        <button onClick={onClick_Mint}>Mint</button>
      </header>
    </div>
  );
}

export default App;
